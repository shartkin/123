#include<iostream>    // Подключение библиотеки для ввода/вывода
#include<math.h>      // Подключение математической библиотеки (лучше использовать <cmath> в C++)
using namespace std;  // Использование стандартного пространства имен

int main()
{
    float p, s;  // Объявление переменных: p - периметр, s - площадь
    
    // Вывод запроса на ввод периметра квадрата
    std::cout << "enter the remit of the square : ";  // "remit" - опечатка, должно быть "perimeter"
    std::cin >> p;  // Ввод значения периметра
    
    // Вычисление площади квадрата по периметру
    // Для квадрата: периметр p = 4 * a, где a - сторона
    // Тогда сторона a = p / 4, а площадь s = a² = (p/4)²
    s = pow(p / 4, 2);  // Использование функции pow для возведения в квадрат
    
    // Вывод результата - площади квадрата
    cout << "\n s= " << s;
    
    return 0;  // Завершение программы
}
