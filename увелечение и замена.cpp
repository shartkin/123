#include <iostream>
#include<math.h>      // Подключение математической библиотеки
using namespace std;

// Функция pomni (от слова "помни" или "pomni" - запомни/измени)
// Принимает параметр x по ссылке (изменения сохраняются)
void pomni(float &x){
    // Тернарный оператор проверяет условие:
    // Если x >= 0, то x = 2 * x
    // Если x < 0, то x = -x (модуль числа)
    x = (x >= 0) ? 2 * x : -x;
}

int main()
{
    float x;  // Объявление переменной x
    
    cout << "enter number" << endl;  // Запрос на ввод числа
    cin >> x;                        // Ввод числа с клавиатуры
    
    pomni(x);           // Вызов функции pomni (x передается по ссылке)
    cout << x << endl;  // Вывод измененного значения x
    
    return 0;           // Завершение программы
}
